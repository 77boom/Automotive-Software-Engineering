/* Includes ******************************************************************/
#include "siu.h"           // SIU初始化函数声明
#include "xpc56el.h"       // SPC56EL寄存器定义（包含SIU结构体）


/* ============================================================================
 * SIU_Init - SIU引脚配置函数
 * ============================================================================
 * 功能：配置MCU所有需要用到的引脚
 *
 * 重要概念：PCR寄存器
 *   PCR = Pad Configuration Register（引脚配置寄存器）
 *   每个引脚都有一个PCR，通过设置PCR的值来定义引脚的功能
 *
 * PCR寄存器各位含义：
 *   Bit 0      - 未使用
 *   Bit 1      - SMC（安全模式控制）
 *   Bit 2      - APC（模拟 pad 控制）
 *   Bit 4-5    - PA（引脚输出分配）
 *   Bit 6      - OBE（输出缓冲器使能）→ 1=输出模式
 *   Bit 7      - IBE（输入缓冲器使能）→ 1=输入模式
 *   Bit 8-9    - DSC/DCS（驱动强度控制）
 *   Bit 10     - ODE（开漏输出使能）
 *   Bit 11     - HYS（输入迟滞）
 *   Bit 12-13  - SRC（压摆率控制）
 *   Bit 14     - WPE（弱上拉/下拉使能）
 *   Bit 15     - WPS（弱上拉/下拉选择）
 *
 * 常用配置值：
 *   0x0100 = 0001 0000 0000 = 输入模式（IBE=1）
 *   0x0200 = 0010 0000 0000 = 输出模式（OBE=1）
 *   0x2500 = 0010 0101 0000 = 模拟输入（APC=1, IBE=1）
 */
void SIU_Init(void)
{
    /*
     * ========================================================================
     * 第一部分：数字输入引脚配置
     * ========================================================================
     * 用于读取按钮和开关的状态
     *
     * 配置方法：SIU.PCR[PCR编号].R = 0x0100;
     * 0x0100 表示 IBE=1（输入模式）
     */

    /* SW1 开关 → D[4] → PCR[52]
     * 前面板上的第一个开关 */
    SIU.PCR[52].R = 0x0100;  // 配置为数字输入

    /* SW2 开关 → D[5] → PCR[53]
     * 前面板上的第二个开关 */
    SIU.PCR[53].R = 0x0100;  // 配置为数字输入

    /* SW3 开关 → D[6] → PCR[54]
     * 前面板上的第三个开关 */
    SIU.PCR[54].R = 0x0100;  // 配置为数字输入

    /* SW4 开关 → D[7] → PCR[55]
     * 前面板上的第四个开关 */
    SIU.PCR[55].R = 0x0100;  // 配置为数字输入

    /* BT1 按钮 → D[12] → PCR[60]
     * 前面板上的第一个按钮 */
    SIU.PCR[60].R = 0x0100;  // 配置为数字输入

    /* BT2 按钮 → D[14] → PCR[62]
     * 前面板上的第二个按钮 */
    SIU.PCR[62].R = 0x0100;  // 配置为数字输入


    /*
     * ========================================================================
     * 第二部分：数字输出引脚配置
     * ========================================================================
     * 用于控制LED的亮灭
     *
     * 配置方法：SIU.PCR[PCR编号].R = 0x0200;
     * 0x0200 表示 OBE=1（输出模式）
     */

    /* P LED（红色）→ D[8] → PCR[56]
     * 前面板上的红色LED（P是Power的缩写） */
    SIU.PCR[56].R = 0x0200;  // 配置为数字输出

    /* Rx LED（黄色）→ D[9] → PCR[57]
     * 前面板上的黄色LED（Rx表示接收指示） */
    SIU.PCR[57].R = 0x0200;  // 配置为数字输出

    /* Tx LED（黄色）→ D[10] → PCR[58]
     * 前面板上的黄色LED（Tx表示发送指示） */
    SIU.PCR[58].R = 0x0200;  // 配置为数字输出

    /* U1 LED（绿色）→ D[11] → PCR[59]
     * 前面板上的绿色LED（用于二进制计数器） */
    SIU.PCR[59].R = 0x0200;  // 配置为数字输出

    /* U2 LED（绿色）→ C[11] → PCR[43]
     * 前面板上的绿色LED（用于二进制计数器） */
    SIU.PCR[43].R = 0x0200;  // 配置为数字输出

    /* U3 LED（绿色）→ A[6] → PCR[6]
     * 前面板上的绿色LED（用于二进制计数器） */
    SIU.PCR[6].R  = 0x0200;  // 配置为数字输出


    /*
     * ========================================================================
     * 第三部分：模拟输入引脚配置
     * ========================================================================
     * 用于读取电位器和光敏电阻的模拟值
     *
     * 配置方法：SIU.PCR[PCR编号].R = 0x2500;
     * 0x2500 = 0010 0101 0000
     *   - Bit 2 (APC) = 1 → 模拟模式
     *   - Bit 7 (IBE) = 1 → 输入模式
     *
     * 注意：模拟引脚需要ADC模块配合使用
     */

    /* POT 电位器 → E[2] → PCR[66]
     * 前面板上的旋钮式电位器
     * 旋转范围：0-4095（12位ADC精度）
     *   - 转到最左边：值接近0
     *   - 转到最右边：值接近4095 */
    SIU.PCR[66].R = 0x2500;  // 配置为模拟输入

    /* LDR 光敏电阻 → C[0] → PCR[32]
     * 前面板上的光敏电阻
     * 受光强度影响：0-4095
     *   - 明亮环境（手电筒照）：值接近0
     *   - 黑暗环境（用手遮住）：值接近4095 */
    SIU.PCR[32].R = 0x2500;  // 配置为模拟输入
}


/* ========================================================================
 * 引脚配置总结表
 * ========================================================================
 *
 * 外设     | 前面板位置 | MCU引脚 | PCR编号 | 配置值  | 说明
 * ---------|-----------|---------|---------|--------|------------------
 * SW1      | 开关1     | D[4]    | 52      | 0x0100 | 数字输入
 * SW2      | 开关2     | D[5]    | 53      | 0x0100 | 数字输入
 * SW3      | 开关3     | D[6]    | 54      | 0x0100 | 数字输入
 * SW4      | 开关4     | D[7]    | 55      | 0x0100 | 数字输入
 * BT1      | 按钮1     | D[12]   | 60      | 0x0100 | 数字输入
 * BT2      | 按钮2     | D[14]   | 62      | 0x0100 | 数字输入
 * P LED    | P灯       | D[8]    | 56      | 0x0200 | 数字输出
 * Rx LED   | Rx灯      | D[9]    | 57      | 0x0200 | 数字输出
 * Tx LED   | Tx灯      | D[10]   | 58      | 0x0200 | 数字输出
 * U1 LED   | U1灯      | D[11]   | 59      | 0x0200 | 数字输出
 * U2 LED   | U2灯      | C[11]   | 43      | 0x0200 | 数字输出
 * U3 LED   | U3灯      | A[6]    | 6       | 0x0200 | 数字输出
 * POT      | 电位器     | E[2]    | 66      | 0x2500 | 模拟输入
 * LDR      | 光敏电阻   | C[0]    | 32      | 0x2500 | 模拟输入
 *
 * ========================================================================
 * GPDO/GPDI寄存器说明
 * ========================================================================
 * 配置完PCR引脚后，使用GPDO和GPDI来读写引脚状态：
 *
 * GPDO[PCR编号] - GPIO Data Output（输出）
 *   用于控制输出引脚（LED）
 *   SIU.GPDO[56].B.PDO = 1;  // 点亮LED
 *   SIU.GPDO[56].B.PDO = 0;  // 熄灭LED
 *
 * GPDI[PCR编号] - GPIO Data Input（输入）
 *   用于读取输入引脚（开关、按钮）
 *   uint8_t state = SIU.GPDI[52].B.PDO;  // 读取SW1状态
 */
